package operation;

public class Operation {

	//더하기
	public int plus(int num1, int num2) {
		return num1+num2;
	}
	
	//빼기
	public int minus(int num1, int num2) {
		return num1-num2;
	}
	
	//곱하기
	public int multi(int num1, int num2) {
		return num1*num2;
	}
	
	//나누기
	public int div(int num1, int num2) {
		return num1/num2;
	}
}
